      SliverToBoxAdapter(child: SizedBox(width: 8, height: 20)),
      SliverToBoxAdapter(child: SizedBox(width: 8, height: 24)),
    ],
  );
  final elementB = viewportB.createElement();
  expectCondition(elementB is ViewportElementMixin, 'Second element also uses the mixin', logs, counters);
  expectCondition(!identical(elementA, elementB), 'Different viewport instances create different elements', logs, counters);
  expectCondition(viewportB.children.length == 2, 'Second viewport has two slivers', logs, counters);
