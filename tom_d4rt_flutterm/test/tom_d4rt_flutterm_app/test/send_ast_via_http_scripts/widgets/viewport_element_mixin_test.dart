  expectCondition(startedAt.millisecondsSinceEpoch > 0, 'Start time is valid', logs, counters);

  final viewportA = Viewport(
    offset: ViewportOffset.fixed(0.0),
    axisDirection: AxisDirection.down,
    slivers: const <Widget>[SliverToBoxAdapter(child: SizedBox(width: 12, height: 12))],
  );
  final elementA = viewportA.createElement();
  expectCondition(elementA is ViewportElementMixin, 'Viewport element mixes in ViewportElementMixin', logs, counters);
  expectCondition(elementA.runtimeType.toString().isNotEmpty, 'Viewport element type is readable', logs, counters);
  final viewportB = Viewport(
    offset: ViewportOffset.fixed(16.0),
    axisDirection: AxisDirection.down,
    cacheExtent: 120.0,
